import UIKit

/// A color enum to store all the app related colors
enum Color {
    static let defaultText = UIColor(named: "defaultTextColor")!
    static let subtleText = UIColor(named: "subtleTextColor")!
    static let background = UIColor(named: "backgroundColor")!
    static let logoBackground = UIColor(named: "logoBackgroundColor")!
    static let logoForeground = UIColor(named: "logoForegroundColor")!
    static let sectionHeaderBackground = UIColor(named: "sectionHeaderBackground")!
}
